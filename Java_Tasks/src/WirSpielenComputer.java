
public class WirSpielenComputer {
    public WirSpielenComputer() {
    }

    public static void main(String[] args) {
        int z2;
        for(int z1 = 10; z1 > -10; z1 -= z2) {
            for(z2 = 1; z2 < 10; z2 *= 2) {
                System.out.println("" + z1 + " / " + z2);
            }
        }

    }
}
